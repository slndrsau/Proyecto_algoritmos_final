public class Actividades {
    private String Id;
    private String IdListaActividades;
    private String Nombre;
    private String Estado;

    /**
     * @return the Id
     */
    public String getId() {
        return Id;
    }

    /**
     * @param Id the Id to set
     */
    public void setId(String Id) {
        this.Id = Id;
    }

    /**
     * @return the IdListaActividades
     */
    public String getIdListaActividades() {
        return IdListaActividades;
    }

    /**
     * @param IdListaActividades the IdListaActividades to set
     */
    public void setIdListaActividades(String IdListaActividades) {
        this.IdListaActividades = IdListaActividades;
    }

    /**
     * @return the Nombre
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * @param Nombre the Nombre to set
     */
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    /**
     * @return the Estado
     */
    public String getEstado() {
        return Estado;
    }

    /**
     * @param Estado the Estado to set
     */
    public void setEstado(String Estado) {
        this.Estado = Estado;
    }
    
    
}
